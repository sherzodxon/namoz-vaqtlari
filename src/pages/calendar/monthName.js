export const monthName = (month) => {
    switch (month) {
        case 0:
            return "Yanvar"
        case 1:
            return "Fevral"
        case 2:
            return "Mart";
        case 3:
            return "Aprel"
        case 4:
            return "May"
        case 5:
            return "Iyun"
        case 6:
            return "Iyul"
        case 7:
            return "Avgust"
        case 8:
            return "Sentyabr"
        case 9:
            return "Oktyabr"
        case 10:
            return "Noyabr"
        case 11:
            return "Dekabr"
        default:
            break;
    }
}